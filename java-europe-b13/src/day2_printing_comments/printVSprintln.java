package day2_printing_comments;

public class printVSprintln {

	public static void main(String[] args) {
		
		System.out.print("one");
		
		System.out.println("two");
		
		System.out.println("three");
		
		System.out.println("four");
		
		System.out.println("  *  ");
		 
		System.out.println(" * *");
		
		System.out.println("*   *");
		
		System.out.println("*****");

	}

}
